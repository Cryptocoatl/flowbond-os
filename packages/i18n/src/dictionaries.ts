export const DICT: Record<Lang, Dict> = { en, es, pt, fr, de, it, ja, zh, ar, hi };

/** Translate `key` for `lang`, falling back to English then the raw key.
 *  `{placeholders}` are replaced from `vars`. */
export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const s = DICT[lang]?.[key] ?? DICT.en[key] ?? key;
  return vars ? s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? "")) : s;
}

/** Read the persisted language from a cookie value. Server-safe — lives in this
 *  (non-"use client") module so server layouts can call it. */
export function langFromCookie(cookieValue: string | undefined | null): {
  lang: Lang;
  hadCookie: boolean;
} {
  const ok = LANGS.some((l) => l.code === cookieValue);
  return { lang: (ok ? cookieValue : "en") as Lang, hadCookie: ok };
}
